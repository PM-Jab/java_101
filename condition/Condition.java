package condition;

public class Condition {
    public static void main(String[] args) {
        int a = 10;
        if (a >= 30) {
            System.out.println("More than 30");
        } else if (a >= 20) {
            System.out.println("More than 20");
        } else {
            System.out.println("Less than 20");
        }
    }
}
